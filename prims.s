myapply:
    push rbp
    mov rbp, rsp

    mov r10,PVAR(-1);num of args
    add r10,3 ;add ret+env+|n|
    mov rbx,r10
    shl r10,3
    mov r9,0
    mov rax,qword[rbp+r10] ;rax points to the list of retaddr+env+|n|+args
    cmp rax,SOB_NIL_ADDRESS
    je complete_apply


    loop_args_into_stack:
        inc r9 ; r9 = |n|
        CAR rcx,rax
        push rcx
        CDR rcx,rax
        cmp rcx,SOB_NIL_ADDRESS
        je before_rev
        CDR rax,rax
        jmp loop_args_into_stack

    before_rev:
    mov r11,0
    mov rcx,r9 
    shl rcx,3
    MALLOC rax,rcx
    shr rcx,3
    jmp loop_rev

    loop_rev:
    cmp rcx,0
    je loop2_args_into_stack
    pop qword[rax+r11*(8)]
    inc r11
    dec rcx
    jmp loop_rev

    loop2_args_into_stack:
    cmp rcx,r9
    je complete_apply
    push qword[rax+rcx*8]
    inc rcx
    jmp loop2_args_into_stack


    complete_apply:
    dec rbx
    cmp rbx,4
    je procedure_apply
    push qword[rbp+rbx*8]
    inc r9
    jmp complete_apply
    
    procedure_apply:
    push r9
    mov rcx,PVAR(0)
    CLOSURE_ENV rax ,rcx
    CLOSURE_CODE rcx ,rcx
    push rax
    push PVAR(-3)
    mov r12,qword[rbp]
    add r9,3
    
    .shft_frm:
    push rax ;to save the old val of rax 
    push rcx
	mov rbx, PVAR(-1)
	mov r10,rbx
	add rbx, 4

  	mov rax,rbp
    sub rax,8
	mov rcx, r9
	looop:
		dec rbx
		mov r11, qword[rax]
		mov qword[rbp+rbx*8], r11
        sub rax,8
		dec rcx
		cmp rcx,0
		jne looop

    pop rcx
	pop rax ;return the old val of rax 
    mov rbx, rax
    inc r9
    shl r9,3
    mov rax,r9
    shr r9,3
    
	add rsp, rax
    dec r9
    add r10,3
    sub r10,r9
    shl r10,3
    mov rax,rbx
	add rsp,r10


    mov rbp,r12
    jmp rcx

    leave
    ret 
    


